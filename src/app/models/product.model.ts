export interface IProduct {
  id: number;
  title: string;
  description: string;
  price: number;
  discountPercentage: number;
  rating: number;
  stock: number;
  brand: string;
  category: string;
  thumbnail: string;
  images: string[];
}

export function sortProductByPrice(p1: IProduct, p2: IProduct) {
  return p1.price - p2.price;
}